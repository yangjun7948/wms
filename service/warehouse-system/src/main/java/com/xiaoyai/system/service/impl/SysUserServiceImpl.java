    /**
     * 批量删除用户信息
     *
     * @param userIds 需要删除的用户ID
     * @return 结果
     */
    @Override
    @Transactional
    public int deleteUserByIds(Long[] userIds) {
        for (Long userId : userIds) {
            checkUserAllowed(new SysUser(userId));
            checkUserDataScope(userId);
        }
        // 删除用户与角色关联
        userRoleMapper.deleteUserRole(userIds);
        // 删除用户与岗位关联
        userPostMapper.deleteUserPost(userIds);
        // 删除个人仓库关联
        iWarehouseService.deletePersonWarehouse(userIds);
        return userMapper.deleteUserByIds(userIds);
    }

    /**
     * 导入用户数据
     *
     * @param userList        用户数据列表
     * @param isUpdateSupport 是否更新支持，如果已存在，则进行更新数据
     * @param operName        操作用户
     * @return 结果
     */
    @Override
    public String importUser(List<SysUser> userList, Boolean isUpdateSupport, String operName) {
        if (StringUtils.isNull(userList) || userList.size() == 0) {
            throw new ServiceException("导入用户数据不能为空！");
        }
        int successNum = 0;
        int failureNum = 0;
        StringBuilder successMsg = new StringBuilder();
        StringBuilder failureMsg = new StringBuilder();
        String password = configService.selectConfigByKey("sys.user.initPassword");
        for (SysUser user : userList) {
            try {
                // 验证是否存在这个用户
                SysUser u = userMapper.selectUserByUserName(user.getUserName());
                if (StringUtils.isNull(u)) {
                    BeanValidators.validateWithException(validator, user);
                    user.setPassword(SecurityUtils.encryptPassword(password));
                    user.setCreateBy(operName);
                    this.insertUser(user);
                    successNum++;
                    successMsg.append("<br/>" + successNum + "、账号 " + user.getUserName() + " 导入成功");
                } else if (isUpdateSupport) {
                    BeanValidators.validateWithException(validator, user);
                    checkUserAllowed(user);
                    checkUserDataScope(user.getUserId());
                    user.setUpdateBy(operName);
                    this.updateUser(user);
                    successNum++;
                    successMsg.append("<br/>" + successNum + "、账号 " + user.getUserName() + " 更新成功");
                } else {
                    failureNum++;
                    failureMsg.append("<br/>" + failureNum + "、账号 " + user.getUserName() + " 已存在");
                }
            } catch (Exception e) {
                failureNum++;
                String msg = "<br/>" + failureNum + "、账号 " + user.getUserName() + " 导入失败：";
                failureMsg.append(msg + e.getMessage());
                log.error(msg, e);
            }
        }
        if (failureNum > 0) {
            failureMsg.insert(0, "很抱歉，导入失败！共 " + failureNum + " 条数据格式不正确，错误如下：");
            throw new ServiceException(failureMsg.toString());
        } else {
            successMsg.insert(0, "恭喜您，数据已全部导入成功！共 " + successNum + " 条，数据如下：");
        }
        return successMsg.toString();
    }

    @Override
    public List<DeptUserTree> deptUserTree() {
        List<DeptUserTree> sysDeptList = iSysDeptService.selectDeptList(new SysDept()).stream().map(t->{
            DeptUserTree deptUserTree= new DeptUserTree();
            deptUserTree.setId(t.getDeptId().toString());
            deptUserTree.setName(t.getDeptName());
            deptUserTree.setParentId(t.getParentId().toString());
            deptUserTree.setType("1");
            deptUserTree.setUserFlag(false);
            return  deptUserTree;
        }).collect(Collectors.toList());
        //部门树
        List<DeptUserTree> _temp =  TreeUtils.buildByRecursiveNode(sysDeptList, Constants.ROOT_DEPT_ID);
        List<DeptUserTree> sysUserList = this.selectUserList(new SysUser()).stream().map(t->{
            DeptUserTree deptUserTree= new DeptUserTree();
            deptUserTree.setId(t.getUserId().toString());
            deptUserTree.setName(t.getNickName());
            deptUserTree.setParentId(t.getDeptId().toString());
            deptUserTree.setType("2");
            deptUserTree.setUserFlag(true);
            return  deptUserTree;
        }).collect(Collectors.toList());
        //部门树添加用户节点，生成新树
        List<DeptUserTree> returnTree = TreeUtils.appendChildNode(_temp,sysUserList);

        return returnTree;
    }

