<script>
export default {
  data() {
    return {
      controlList: [],
    };
  },
  methods: {
    dragstart(ev, name, type) {
      let control = {
        name: name,
        type: type,
        select: false,
      };
      ev.dataTransfer.setData("dragContent", JSON.stringify(control));
    },
    allowDrop(ev) {
      ev.preventDefault();
    },
    drop(ev) {
      event.preventDefault();
      debugger;
      let control = event.dataTransfer.getData("dragContent");
      this.controlList.push(JSON.parse(control));
    },
    selectControl(item) {
      this.controlList.forEach((t) => {
        t.select = false;
      });
      item.select = true;
    },
  },
};
</script>
